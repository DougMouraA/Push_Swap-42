#include "libft.h"

int	ft_isalpha(int c)
{
	if (c < 0)
	{
		return (0);
	}
	if (c < 'A')
	{
		return (0);
	}
	else if (c > 'Z' && c < 'a')
	{
		return (0);
	}
	else if (c > 'z')
	{
		return (0);
	}
	else
	{
		return (1);
	}
}
