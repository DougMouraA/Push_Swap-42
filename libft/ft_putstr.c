#include "libft.h"

int	ft_putstr(const char *str)
{
	size_t	i;

	i = 0;
	if (!str)
	{
		str = "(null)";
	}
	while (str[i] != '\0')
	{
		ft_putchar (str[i]);
		i++;
	}
	return (i);
}
